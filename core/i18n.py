class LanguageManager:
    def __init__(self):
        self.current_lang = 'zh'

    def set_language(self, lang):
        if lang in TRANSLATIONS:
            self.current_lang = lang

    def tr(self, key):
        """Translate a key for current language.

        Behavior:
        - If key exists in current language: return it.
        - If key missing:
            * If current language is 'en': return key (NO zh fallback; expose missing translations).
            * Otherwise: try fallback to English, then to Chinese, then key.
        """
        cur = TRANSLATIONS.get(self.current_lang, {})
        if key in cur:
            return cur[key]

        # Do NOT fallback to zh in English mode (avoid mixed-language UI).
        if self.current_lang == 'en':
            return key

        en = TRANSLATIONS.get('en', {})
        if key in en:
            return en[key]

        zh = TRANSLATIONS.get('zh', {})
        return zh.get(key, key)


